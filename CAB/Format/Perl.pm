sub new {
  my $that = shift;
  my $fmt = bless({
		   ##-- I/O common
		   utf8 => undef, ##-- n/a

		   ##-- Input
		   #doc => undef,

		   ##-- Output
		   dumper => Data::Dumper->new([])->Purity(1)->Terse(0)->Deepcopy(1),
