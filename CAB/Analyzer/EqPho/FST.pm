## $obj = CLASS_OR_OBJ->new(%args)
##  + object structure: see DTA::CAB::Analyzer::Automaton::Gfsm, DTA::CAB::Analyzer::Automaton
sub new {
  my $that = shift;
  my $aut = $that->SUPER::new(
			      ##-- overrides
			      #tolower => 0,
			      #check_symbols => 0,

			      ##-- analysis selection
			      #analysisClass => 'DTA::CAB::Analyzer::EqPho::FST::Analysis',
			      analyzeSrc     => 'lts',
			      analyzeDst     => 'eqpho',
			      attInput       => 1,
			      wantAnalysisLo => 1,
			      allowTextRegex => '(?:^[[:alpha:]\-]*[[:alpha:]]+$)|(?:^[[:alpha:]]+[[:alpha:]\-]+$)',

			      ##-- user args
			      @_
			     );
  return $aut;
}

